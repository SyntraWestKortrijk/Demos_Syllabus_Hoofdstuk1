using System;

namespace HalloWereldMetVariabeleVoorbeeld
{
    class Program
    {
        static void Main()
        {
            string groet;               // declaratie van de variabele
            groet = "Hallo wereld";     // initialisatie van de variabele
            Console.WriteLine(groet);   // uitlezen van de variabele

            groet = "Hallo Gent";       // inhoud veranderen via toekenning
            Console.WriteLine(groet);   // uitlezen van de variabele
        }

    }
}
